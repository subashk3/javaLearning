package testprograms;

public class FloatAndDouble {

	public static void main(String[] args) {

		float a = 6.5f;
		double b = 6.5d;
		if (a == b) {

			System.out.println("6.5 float and double equal");
		} else
			System.out.println("6.5 float and double not equal");

		a = 6.1f;
		b = 6.1d;

		if (a == b) {

			System.out.println("6.1 float and double equal");
		} else
			System.out.println("6.1 float and double not equal");

		a = 6.0f;
		b = 6.0d;
		if (a == b) {

			System.out.println("6.5 float and double equal");
		} else
			System.out.println("6.5 float and double not equal");

	}

}
